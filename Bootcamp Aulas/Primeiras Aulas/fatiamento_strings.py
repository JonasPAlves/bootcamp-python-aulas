nome = "Jonas Pereira Alves"

print (nome[0])# índice 0

print (nome[-2])# retorna a penúltima letra

print (nome[:5])# do ínicio até a 5ª posição

print (nome[14:])# da 14ª posição até  fim

print (nome[5:9]) #inicia no 5 até o 9

print (nome[6:12:2]) #6 = start(inicio), 12 = stop(fim), 2 step(passo) 

print (nome[:]) # retorna a string inteira

print (nome[::-1])
